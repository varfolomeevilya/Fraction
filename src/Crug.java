public class Crug {
    private int radius=4;
    public int area;
    private int perimeter=314;

    public int getPerimeter(){
         return perimeter=2*perimeter*radius;
    }
    public int getArea(){
         return area=perimeter*radius*2;
    }
    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }
}
